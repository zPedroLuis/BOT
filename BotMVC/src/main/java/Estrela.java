
public class Estrela {
	
	private String nome;
	private String field;
	
	
	
	public Estrela(String nome, String field) {
		this.nome = nome;
		this.field = field;
	}
	
	public String getName() {
		return nome;
	}
	public void setName(String nome) {
		this.nome = nome;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	
	

}
